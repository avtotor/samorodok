/**
 * Промпт и инструкции для структурированного ответа (как в старом LangChain StructuredOutputParser).
 * 
 *  "explanation": <строка на русском языке — краткое пояснение оценки>
 */
export function buildCheckerPrompt(resumeText, jobDescription) {
  const formatInstructions = `
Ты ОБЯЗАН вернуть ТОЛЬКО один валидный JSON-объект (без markdown, без \`\`\`, без комментариев, без текста до или после скобок).

Обязательные правила JSON:
- Все три ключа в двойных кавычках: "score", "value", "explanation" — ключ "explanation" пиши целиком на одной строке, без переноса посередине слова.
- Значение "explanation" — только внутри двойных кавычек; внутри строки символ " экранируй как \\". Перенос строки в тексте — как \\n (не настоящий Enter внутри кавычек ключа).
- "value" только true или false (без кавычек).
- "score" — целое число 0–100 без кавычек.

Пример формата (смысл вымышленный):
{"score":72,"value":true,"explanation":"Первое предложение.\\nВторое предложение."}

Ключи и типы:
{
  "score": <число 0–100 — балл соответствия резюме требованиям вакансии>,
  "value": <boolean — подходит ли резюме для вакансии>,
  "explanation": <строка на русском в кавычках — 1–3 предложения, между предложениями \\n>
}
`.trim();

  return `
# Инструкция:
Ты проверяешь резюме, сравнивая его с описанием вакансии ниже. Твоя задача — строго определить, подходит ли резюме для этой вакансии, и кратко объяснить почему (поле "explanation").

${formatInstructions}

Никакого текста за пределами JSON-объекта. Не пиши пояснений до или после JSON.

# РЕЗЮМЕ:
${resumeText}

# ОПИСАНИЕ ВАКАНСИИ:
${jobDescription}

# ТВОЙ ОТВЕТ:
`.trim();
}
